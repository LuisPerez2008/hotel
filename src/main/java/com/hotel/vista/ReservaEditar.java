    private void txtDocumentoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDocumentoKeyPressed
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            String dni = txtDocumento.getText();
            Huesped hp = huespedDao.BuscarPorDni(dni);
            if (hp != null) {
                txtIdCliente.setText(String.valueOf(hp.getId()));
                txtNombreCliente.setText(hp.getNombre());
            } else {
                // Manejar el caso donde no se encuentra el huésped
                txtIdCliente.setText("");
                txtNombreCliente.setText("");
                JOptionPane.showMessageDialog(this, "No se encontró un huésped con el DNI proporcionado", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_txtDocumentoKeyPressed

