    private void btnConfirmarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarReservaActionPerformed
        if (validarceldas()) {
            int pregunta = JOptionPane.showConfirmDialog(null, "Actualizar Reserva?");
            if (pregunta == 0) {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                try {
                    // Convertir el String a java.util.Date
                    Date fechaReserva = sdf.parse(txtFechaFormatoEntrada.getText());
                    Date fechaSalida = sdf.parse(txtFechaFormatoSalida.getText());
                    Reserva reserva = new Reserva();
                    reserva.setId(Integer.parseInt(txtIdReserva.getText()));
                    reserva.setId_usuario(Integer.parseInt(txtIdUsuario.getText()));
                    reserva.setId_huesped(Integer.parseInt(txtIdCliente.getText()));
                    reserva.setFecha_reserva(fechaReserva);
                    reserva.setFecha_salida(fechaSalida);
                    reservaDao.actualizar(reserva);

                } catch (ParseException e) {
                    System.err.println("Error al convertir la fecha: " + e.getMessage());
                }
                //Iniciar Detalle
                int id_habitacion = Integer.parseInt(txtIdHabitacion.getText());
                int id_reserva = Integer.parseInt(txtIdReserva.getText());
                int id_metodopago = (cbxMetodoPago.getSelectedIndex() + 1);
                double extras = actualizarPrecioDetalle(Integer.parseInt(txtIddetalle.getText()));
                double precio = (Double) (model.getValueAt(0, 5));
                double precio_Total = precio + extras;
                Detalle detalle = new Detalle();
                detalle.setId(Integer.parseInt(txtIddetalle.getText()));
                detalle.setId_habitacion(id_habitacion);
                detalle.setId_reserva(id_reserva);
                detalle.setId_metodoPago(id_metodopago);
                detalle.setPrecioTotal(precio_Total);
                detalleDao.actualizar(detalle);
                JOptionPane.showMessageDialog(null, "Actualizado");
                setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Llenar todos los campos", "Aviso", HEIGHT);
        }

       
    }//GEN-LAST:event_btnConfirmarReservaActionPerformed
    public double actualizarPrecioDetalle(int id){
        Detalle detall = detalleDao.buscarPorId(id);
        double precio_extras = extraDao.sumaDeextrasXdetalle(detall.getId());
        return precio_extras;
        
    }
    
    public void ObtenerFecha() {
        Date date = FechaEntrada.getDate();
        Date date2 = FechaSalida.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String fechaFormateada1 = sdf.format(date);
        String fechaFormateada2 = sdf.format(date2);
        txtFechaFormatoEntrada.setText(fechaFormateada1);
        txtFechaFormatoSalida.setText(fechaFormateada2);

    }

    public void llenarTabla() {
        model = (DefaultTableModel) tableReseerva.getModel();
        String num = txtNumhabitacion.getText();

        String fEntrda = txtFechaFormatoEntrada.getText();
        String fSalida = txtFechaFormatoSalida.getText();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");

        // Convertir String a LocalDate
        LocalDate fechaInicio = LocalDate.parse(fEntrda, formatter);
        LocalDate fechaFin = LocalDate.parse(fSalida, formatter);
        int diasEntre = (int) ChronoUnit.DAYS.between(fechaInicio, fechaFin);
        double precioxDia = Double.parseDouble(txtPrecio.getText());
        double total = diasEntre * precioxDia;

        model.addRow(new Object[]{num, fEntrda, fSalida, diasEntre, precioxDia, total});
    }

    public boolean validarceldas() {
        if (!"".equals(txtNombreCliente.getText()) && !"".equals(txtFechaFormatoEntrada.getText()) && !"".equals(txtFechaFormatoSalida.getText()) && tableReseerva.getRowCount() > 0) {
            return true;
        } else {
            return false;
        }
    }

